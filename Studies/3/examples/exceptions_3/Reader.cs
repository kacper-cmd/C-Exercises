using System;
static class Reader
{

    public static string readString() { return Console.ReadLine(); }
    public static int readInt() { return int.Parse(Console.ReadLine()); }
    public static byte readByte() { return byte.Parse(Console.ReadLine()); }
    public static decimal readDecimal() { return decimal.Parse(Console.ReadLine()); }
    public static float readFloat() { return float.Parse(Console.ReadLine()); }
}